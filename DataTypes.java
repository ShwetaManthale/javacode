package basics;

public class DataTypes {
	public static void main(String[] args) {
		byte a;
		a=10;
		a=20;
		System.out.println(a);
		
		int c;
		c=1000;
		c=20000;
		System.out.println(c);
		
		char g;
		g='a';
		g='g';
		System.out.println(g);
		
		boolean h;
		h=true;
		h= false;
		System.out.println(h);
		
		String i;
		i="shweta";
		i="shweta manthale";
		System.out.println(i);
}
		
	}

